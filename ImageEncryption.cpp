void keyExpantion(unsigned char k[]) {
    const unsigned char roundConstant[10] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36};
    int i,j;
    // As first 128 bits word are same
    for(i=0 ; i<4 ; i++) {
        word[i][0] = k[i*4+0];
        word[i][1] = k[i*4+1];
        word[i][2] = k[i*4+2];
        word[i][3] = k[i*4+3];
    }

    char w0[4];
    for(i=1 ; i<11 ; i++) {
        // g function
        // one byte left circular shift & sBox substitution
        w0[0] = sbox[word[i*4-1][1]];
        w0[1] = sbox[word[i*4-1][2]];
        w0[2] = sbox[word[i*4-1][3]];
        w0[3] = sbox[word[i*4-1][0]];

        // X-OR with round constant
        w0[0] = w0[0] ^ roundConstant[i-1];


        // w4 = w0 X-OR g(w3)
        word[i*4+0][0] = word[(i-1)*4+0][0] ^ w0[0];
        word[i*4+0][1] = word[(i-1)*4+0][1] ^ w0[1];
        word[i*4+0][2] = word[(i-1)*4+0][2] ^ w0[2];
        word[i*4+0][3] = word[(i-1)*4+0][3] ^ w0[3];

        // w5 = w1 X-OR w4
        word[i*4+1][0] = word[(i-1)*4+1][0] ^ word[i*4+0][0];
        word[i*4+1][1] = word[(i-1)*4+1][1] ^ word[i*4+0][1];
        word[i*4+1][2] = word[(i-1)*4+1][2] ^ word[i*4+0][2];
        word[i*4+1][3] = word[(i-1)*4+1][3] ^ word[i*4+0][3];

        // w6 = w2 X-OR w5
        word[i*4+2][0] = word[(i-1)*4+2][0] ^ word[i*4+1][0];
        word[i*4+2][1] = word[(i-1)*4+2][1] ^ word[i*4+1][1];
        word[i*4+2][2] = word[(i-1)*4+2][2] ^ word[i*4+1][2];
        word[i*4+2][3] = word[(i-1)*4+2][3] ^ word[i*4+1][3];

        // w7 = w3 X-OR w6
        word[i*4+3][0] = word[(i-1)*4+3][0] ^ word[i*4+2][0];
        word[i*4+3][1] = word[(i-1)*4+3][1] ^ word[i*4+2][1];
        word[i*4+3][2] = word[(i-1)*4+3][2] ^ word[i*4+2][2];
        word[i*4+3][3] = word[(i-1)*4+3][3] ^ word[i*4+2][3];
    }
}


